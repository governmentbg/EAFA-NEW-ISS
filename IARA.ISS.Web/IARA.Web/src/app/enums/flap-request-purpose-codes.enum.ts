export enum FlapRequestPurposeCodes {
    /**
     * Stop processing of fishing authorisation request
     * */
    STOP,

    /**
     * Modify fishing authorisation
     * */
    MOD,

    /**
     * Apply for the new fishing authorisation
     * */
    APP,

    /**
     * Notify the vessel
     * */
    NOT,

    /**
     * Apply for the renewal of the fishing authorisation
     * */
    REN,

    /**
     * Terminate fishing authorisation
     * */
    TER,

    /**
     * Activate fishing authorisation
     * */
    ACT,

    /**
     * Suspend fishing authorisation
     * */
    SUS
}